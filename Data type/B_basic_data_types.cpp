#include<bits/stdc++.h>
using namespace std;

int main()
{
    int intValue;
    long long longLongValue;
    char charValue;
    float floatValue;
    double doubleValue;

    // Read the five values from the input stream
    cin >> intValue >> longLongValue >> charValue >> floatValue >> doubleValue;

    // Print each value on a new line
    cout << intValue << endl;
    cout << longLongValue << endl;
    cout << charValue << endl;
    cout << floatValue << endl;
    cout << doubleValue << endl;

    return 0;
}
